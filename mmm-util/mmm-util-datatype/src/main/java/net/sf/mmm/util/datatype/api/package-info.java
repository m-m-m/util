/**
 * Provides common datatypes.
 * <a name="documentation"/><h2>Datatype API</h2> 
 * Java offers very generic datatypes like {@link java.lang.String}, 
 * {@link java.lang.Boolean}, or {@link java.lang.Number}. For structured data 
 * models it is often desired to use more specific 
 * {@link net.sf.mmm.util.lang.api.Datatype datatypes}. While most of them are 
 * custom datatypes specific for a particular project or product, there are
 * also some very common datatypes missing in the JDK. This package and its sub 
 * packages provide common datatypes to fill this gap.
 */
package net.sf.mmm.util.datatype.api;

